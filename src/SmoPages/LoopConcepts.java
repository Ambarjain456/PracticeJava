package SmoPages;

public class LoopConcepts {

	public static void main(String[] args) {
		
		
		/*
		 * int i =1; while (i<=10) { System.out.println(i); i= i+1;
		 * 
		 * } System.out.println("*****");
		 * 
		 * for( int j=10; j>=1; j--) { System.out.println(j); }
		 */

		for (int k=1; k<=10; k++ ) {
			System.out.println(k);
		}
	}

}
